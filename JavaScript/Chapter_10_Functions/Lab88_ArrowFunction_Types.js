// No Argument No return type

const fn1 = () => console.log("Hi fn1");

fn1()

// With Argument No return

const fn2 = (name) => console.log(`Hi`, name)
fn2("Kasi")

// No Argument with Return

const sub = (a, b) => a - b
console.log(sub(8, 5))

// With Argument with Return
const sum = (a, b) => a + b
console.log(sum(5, 6)); 
